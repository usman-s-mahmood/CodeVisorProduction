class AlgorithmProblem(models.Model):
    # Difficulty Choices
    DIFFICULTY_CHOICES = [
        ('beginner', 'Beginner'),
        ('intermediate', 'Intermediate'),
        ('advanced', 'Advanced'),
    ]

    # Core Metadata
    problem_id = models.IntegerField(unique=True, help_text="The exact ID from the JSON batch.")
    title = models.CharField(max_length=255)
    slug = models.SlugField(max_length=255, unique=True, db_index=True)
    difficulty_level = models.CharField(max_length=20, choices=DIFFICULTY_CHOICES)
    elo_requirement = models.IntegerField(help_text="e.g., 800, 1200, 1600")
    
    # Tags stored as a JSON array of strings: e.g., ["Arrays", "Two Pointers"]
    topic_tags = models.JSONField(default=list, blank=True)

    # Problem Content
    problem_description = models.TextField()
    starter_code = models.TextField(blank=True, default="def solution():\n    pass")
    reference_solution = models.TextField()
    
    # Grading & Support
    key_concepts_to_check = models.TextField(help_text="Instructions for the Cohere LLM Judge.")
    progressive_hints = models.JSONField(default=list, blank=True, help_text="List of hint strings.")

    # --- NEW: AI Generation Tracking ---
    is_ai_generated = models.BooleanField(default=False, help_text="True if generated by command-a-03-2025 API.")
    generated_by = models.ForeignKey(
        User, 
        on_delete=models.CASCADE,  # Deletes AI problems if the user deletes their account to save DB space
        null=True, 
        blank=True, 
        related_name='generated_problems'
    )

    # Timestamps for your database records
    created_at = models.DateTimeField(auto_now_add=True)
    updated_at = models.DateTimeField(auto_now=True)

    class Meta:
        ordering = ['problem_id']
        verbose_name = "Algorithm Problem"
        verbose_name_plural = "Algorithm Problems"

    def __str__(self):
        prefix = "[AI] " if self.is_ai_generated else ""
        return f"{prefix}[{self.problem_id}] {self.title} - {self.elo_requirement} Elo"

class ProblemSubmission(models.Model):
    user = models.ForeignKey(User, on_delete=models.CASCADE, related_name='submissions')
    problem = models.ForeignKey(AlgorithmProblem, on_delete=models.CASCADE, related_name='submissions')
    
    submitted_code = models.TextField()
    passed = models.BooleanField(default=False, help_text="True if Cohere marked it as perfectly correct.")
    llm_feedback = models.TextField(help_text="The advice generated by the AI judge.")
    
    created_at = models.DateTimeField(auto_now_add=True)

    class Meta:
        ordering = ['-created_at']

    def __str__(self):
        status = "PASSED" if self.passed else "FAILED"
        return f"[{status}] {self.user.username} - {self.problem.title}"

